#include "libft.h"

int		ft_count_nb(size_t nb)
{
	size_t	i;
	int		j;

	i = 1;
	j = 0;
	if ((int)nb < 0)
	{
		nb = nb * -1;
		j++;
	}
	if (nb == 0 || nb == 1)
		j++;
	while (i < nb)
	{
		i = i * 10;
		j++;
	}
	return (j);
}
