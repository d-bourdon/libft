#include "libft.h"

int		ft_lexincalc(char const *s, size_t n)
{
	size_t	i;
	size_t	tt;

	i = 0;
	tt = 0;
	while (s[i] != 0 && i < n)
	{
		tt += (size_t)s[i];
		i++;
	}
	return (tt);
}
