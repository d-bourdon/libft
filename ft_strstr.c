#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int		i[3];
	char	*ts1;
	char	*ts2;

	i[0] = -1;
	i[1] = 0;
	i[2] = 0;
	ts1 = (char *)s1;
	ts2 = (char *)s2;
	if (s2[0] == 0)
		return (ts1);
	while (ts1[++(i[0])] != '\0')
		if (ts1[i[0]] == ts2[i[1]])
		{
			i[2] = i[0];
			while (ts2[i[1]] != '\0' && ts2[i[1]] == ts1[i[0]++])
				i[1]++;
			if (ts2[i[1]] == '\0')
				return (&ts1[i[2]]);
			i[0] = i[2];
			i[1] = 0;
		}
	return (NULL);
}
