#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int		i;

	i = 0;
	while (i < (int)(ft_strlen(src) + 1))
	{
		dst[i] = src[i];
		i++;
	}
	return (dst);
}
